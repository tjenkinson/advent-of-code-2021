const getInvalidCharInLine = (line) => {
  const stack = [];
  for (const char of line.split('')) {
    const closing = closeByOpen.get(char);
    const opening = openByClose.get(char);
    if (closing) {
      stack.push(closing);
    } else if (opening) {
      const validClosing = stack.pop();
      if (validClosing !== char) {
        return char;
      }
    } else {
      throw new Error('oops');
    }
  }
  return null;
};

