const lines = input.split('\n').filter(Boolean);

const closeByOpen = new Map();
closeByOpen.set('(', ')');
closeByOpen.set('[', ']');
closeByOpen.set('{', '}');
closeByOpen.set('<', '>');

const openByClose = new Map();
for (const [key, value] of closeByOpen) {
  openByClose.set(value, key);
}

const findMissingEnd = (line) => {
  const stack = [];
  for (const char of line.split('')) {
    const closing = closeByOpen.get(char);
    const opening = openByClose.get(char);
    if (closing) {
      stack.push(closing);
    } else if (opening) {
      const validClosing = stack.pop();
      if (validClosing !== char) {
        return null;
      }
    } else {
      throw new Error('oops');
    }
  }

  return stack.reverse().join('')
};

const stack = [];
const ends = lines.map((line) => {
  return findMissingEnd(line);
}).filter(Boolean);

const scores = new Map();
scores.set(')', 1);
scores.set(']', 2);
scores.set('}', 3);
scores.set('>', 4);

const calculateLineScore = (end) => {
  let score = 0;
  for (const char of end.split('')) {
    score *= 5;
    score += scores.get(char);
  }
  return score;
}

const scoreByLine = ends.map((end) => calculateLineScore(end));
scoreByLine.sort((a, b) => a - b);
